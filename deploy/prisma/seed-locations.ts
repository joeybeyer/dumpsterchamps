async function seedLocations() {
  console.log('Seeding business locations...');

  for (const location of BUSINESS_LOCATIONS) {
    await prisma.businessLocation.upsert({
      where: { slug: location.slug },
      update: location,
      create: location,
    });
    console.log(`  ✓ ${location.name}, ${location.stateAbbr}`);
  }

  console.log(`\nSeeded ${BUSINESS_LOCATIONS.length} business locations`);
}

